import numpy as np

a = np.mat([1, 2, 2])
print(a)  #将数组array转化为矩阵matrix 一维数组不是一维矩阵  [1, 2, 2]！=[[1, 2, 2]]

print(a[0,1])  #矩阵有两个维度

# a = np.mat([[[1], [2], [3]],[[1], [2], [3]]])  矩阵只能是两维的，数组可以是多维的

a = np.mat([[1, 2, 2],[3, 6, 3]])  #必须加逗号
print(a[[0, 1 ,0]])  #对矩阵进行数组索引，相当于提取对应的行数组成新矩阵[[1 2 2][3 6 3][1 2 2]]